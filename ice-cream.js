// Arrays - Ice-cream

const iceCreamFlavours = [
  'chocolate',
  'vanilla',
  'cookies and cream',
  'rocky road',
  'strawberry',
];
console.log(iceCreamFlavours);

// add 'root beer' to the array via .push();
iceCreamFlavours.push('root beer');
console.log(iceCreamFlavours);

// log first flavour in array
console.log(iceCreamFlavours[0]);

// log the last flavour in array
console.log(iceCreamFlavours[5]);

// log total number of flavours via the .length property
console.log(iceCreamFlavours.length);
